package CodePractice;

public class DataType {

	public static void main(String[] args)
	{
		byte a = 100;
		//byte l = 128;  //as 128 is max value of byte ,it gives compilation error
		short b = 4567;
		int c = 93845;
		long d = 987654321;
		float e = 567.98f;   //Note:(Mandatory) decimal values should end with f
	    double f = 3.14;
	    float g = 10f;    //non-decimal can also be stored in float and double as well
	    double h = 57893;
	    char i = 's';
	    boolean j = true;
	    String k = "My name is sujani";
	    
		System.out.println("Hello team");
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println(i);
		System.out.println(j);
		System.out.println(k);

	}

}
