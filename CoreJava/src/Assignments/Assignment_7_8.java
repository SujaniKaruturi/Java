package Assignments;
/*
 Ass7_8. Run an infinite for loop using increment,decrement loop
 */
public class Assignment_7_8 
{

	public static void main(String[] args) 
	{
		int i;
		for(i=50;i>10;i++)
		{
			System.out.println("Increment infinite loop");
		}
		
		for(i=1;i<60;i--)
		{
			System.out.println("Decrement infinite loop");
		}	

	}

}
